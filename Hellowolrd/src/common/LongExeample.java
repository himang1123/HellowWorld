package common;

public class LongExeample {
	public static void main(String[] args) {
		long var1 = 10;
		long var2 = 20L; //L은 long형 데이터라고 표시하는것
		//long var3 = 100000000000000000; -> int의 범위를 벗어나는 값은 뒤에 L을 붙여주는것이 좋다.
		long var4 = 100000000000000000L;
		System.out.println(var4);
		
	}
}
